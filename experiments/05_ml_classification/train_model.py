#!/usr/bin/env python3
"""Train an ONNX model for ML protocol classification (Experiment 5).

Reads the synthetic PCAP and labels generated by pcap/generate_pcap.py,
extracts Wang et al. features (first 10 packet lengths + first 10 packet
directions per flow), trains a RandomForestClassifier, and exports to ONNX
with the exact tensor names CensorLab expects:
  - Input:  "float_input"   shape [1, 20]
  - Output: "probabilities"  shape [1, 2]  (float32)

Usage:
    python3 train_model.py [--pcap pcap/test.pcap] [--labels pcap/labels.csv]
                           [--output model.onnx]
"""

import argparse
import csv
import os
import sys

import numpy as np
from scapy.all import rdpcap, TCP
from sklearn.ensemble import RandomForestClassifier

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
WINDOW_SIZE = 10  # must match censor.py


def extract_flows(pcap_path, labels_path):
    """Read PCAP and labels, return (X, y) arrays.

    Each flow is represented by 20 features:
      [len_0, len_1, ..., len_9, dir_0, dir_1, ..., dir_9]
    where dir is 0.0 for client->server and 1.0 for server->client.
    """
    packets = rdpcap(pcap_path)

    with open(labels_path) as f:
        reader = csv.DictReader(f)
        labels = list(reader)

    client_ip = "10.0.0.1"

    X = []
    y = []

    for row in labels:
        first_idx = int(row["first_packet_index"])
        num_pkts = int(row["num_packets"])
        cls = row["class"]

        lengths = []
        directions = []

        for i in range(num_pkts):
            pkt = packets[first_idx + i]
            if not pkt.haslayer(TCP):
                continue
            payload = bytes(pkt[TCP].payload)
            lengths.append(float(len(payload)))
            # Direction: 0.0 = client->server, 1.0 = server->client
            if pkt["IP"].src == client_ip:
                directions.append(0.0)
            else:
                directions.append(1.0)

        # Pad or truncate to WINDOW_SIZE
        lengths = (lengths[:WINDOW_SIZE] +
                   [0.0] * max(0, WINDOW_SIZE - len(lengths)))
        directions = (directions[:WINDOW_SIZE] +
                      [0.0] * max(0, WINDOW_SIZE - len(directions)))

        features = lengths + directions
        X.append(features)
        # Class 0 = blocked so that result[0] (prob of class 0) is the
        # blocked probability, matching censor.py's `result[0] > 0.5` check.
        y.append(0 if cls == "blocked" else 1)

    return np.array(X, dtype=np.float32), np.array(y, dtype=np.int64)


def export_onnx(clf, output_path):
    """Export sklearn classifier to ONNX with CensorLab-compatible names."""
    from skl2onnx import convert_sklearn
    from skl2onnx.common.data_types import FloatTensorType
    import onnx

    initial_type = [("float_input", FloatTensorType([1, WINDOW_SIZE * 2]))]
    onx = convert_sklearn(
        clf,
        initial_types=initial_type,
        options={id(clf): {"zipmap": False}},
        target_opset=11,
    )

    # Rename the probability output to "probabilities" for CensorLab
    for output in onx.graph.output:
        if output.name == "output_probability":
            output.name = "probabilities"
    # Also rename inside the graph nodes that reference this output
    for node in onx.graph.node:
        for i, out in enumerate(node.output):
            if out == "output_probability":
                node.output[i] = "probabilities"

    onnx.save(onx, output_path)
    print(f"ONNX model saved to {output_path}")

    # Verify
    model = onnx.load(output_path)
    input_names = [inp.name for inp in model.graph.input]
    output_names = [out.name for out in model.graph.output]
    print(f"  Inputs:  {input_names}")
    print(f"  Outputs: {output_names}")


def main():
    parser = argparse.ArgumentParser(
        description="Train ONNX model for ML classification experiment")
    parser.add_argument("--pcap", default=os.path.join(SCRIPT_DIR, "pcap", "test.pcap"))
    parser.add_argument("--labels", default=os.path.join(SCRIPT_DIR, "pcap", "labels.csv"))
    parser.add_argument("--output", default=os.path.join(SCRIPT_DIR, "model.onnx"))
    parser.add_argument("--seed", type=int, default=42)
    args = parser.parse_args()

    if not os.path.exists(args.pcap):
        print(f"ERROR: PCAP not found at {args.pcap}", file=sys.stderr)
        print("Run: python3 pcap/generate_pcap.py", file=sys.stderr)
        sys.exit(1)
    if not os.path.exists(args.labels):
        print(f"ERROR: Labels not found at {args.labels}", file=sys.stderr)
        sys.exit(1)

    print("Extracting features from PCAP...")
    X, y = extract_flows(args.pcap, args.labels)
    n_blocked = int((y == 0).sum())
    n_normal = int((y == 1).sum())
    print(f"  {len(X)} flows, {n_blocked} blocked, {n_normal} normal")
    print(f"  Feature shape: {X.shape}")

    print("Training RandomForestClassifier...")
    clf = RandomForestClassifier(
        n_estimators=100,
        max_depth=10,
        random_state=args.seed,
    )
    clf.fit(X, y)

    # Quick accuracy check on training data
    acc = clf.score(X, y)
    print(f"  Training accuracy: {acc:.4f}")

    print("Exporting to ONNX...")
    export_onnx(clf, args.output)
    print("Done.")


if __name__ == "__main__":
    main()
